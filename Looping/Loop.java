public class Loop {
    public static void main(String[] args) {
        //While Loop;
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }

        //For Loop
        for (int a = 0; a < 5; a++) {
            System.out.println(i);
          }
    }
        
}
